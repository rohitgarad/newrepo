package Encapsulation;

public class StudentsClass {
	
	private String Student_name;
	private int Student_roll_no;
	private char Student_Division;
	private int Students_marks;
	private String Result;
	
	
	public String getStudent_name() {
		return Student_name;
	}
	public void setStudent_name(String student_name) {
		Student_name = student_name;
	}
	public int getStudent_roll_no() {
		return Student_roll_no;
	}
	public void setStudent_roll_no(int student_roll_no) {
		Student_roll_no = student_roll_no;
	}
	public char getStudent_Division() {
		return Student_Division;
	}
	public void setStudent_Division(char student_Division) {
		Student_Division = student_Division;
	}
	public int getStudents_marks() {
		return Students_marks;
	}
	public void setStudents_marks(int students_marks) {
		Students_marks = students_marks;
	}
	public String getResult() {
		return Result;
	}
	public void setResult(String result) {
		Result = result;
	}
	

	
   
}
